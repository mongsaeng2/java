public class firstClass {

	public static void main(String[] args) {
		
		/* Access Modifiers
		 * 
		 * The 'public' Java keyword is what's called an access modifier.
		 * 		An access modifier allows us to define which parts of our code,
		 *  	 or even someone else's code, can access a particular element.
		 *  
		 * The 'class' keyword is used to define a class.
		 * 		The class name will be the text following the keyword, so firstClass in this case.
		 * 		Notice the left and right curly braces, they are used to define the class code block, or class body.
		 * */
		
		/* What is a Method
		 * 
		 * A method is a collection of statements, one or more, that perform an operation.
		 * We'll be using a special method called the main method, that Java looks for when running a program.
		 * It's the entry point for any Java code, and Java looks for the main method to start and run the program when we use it.*/
		
		System.out.println("Hello World");
		System.out.println("Hello, Minji:)");
	}
}
