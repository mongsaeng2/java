public class variables {

	public static void main(String[] args) {
		
		/*
		 * Variables
		 * variables are a way to store information in our computer*/
		
		int myFirstNumber = 5;
		System.out.println(myFirstNumber);
		System.out.println("myFirstNumber");
		
		myFirstNumber = 10;
		System.out.println(myFirstNumber);
		
		myFirstNumber = 1000;
		System.out.println(myFirstNumber);

		myFirstNumber = 10 + 5;
		System.out.println(myFirstNumber);
		
		myFirstNumber = (10+5) + (2*10);
		System.out.println(myFirstNumber);
		
	}

}
